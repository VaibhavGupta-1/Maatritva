@Composable
fun WeekScreen(
    navController: NavController,
    weekText: String,
    string: String
) {
    Column {
        AppHeader(text = weekText)
        Spacer(Modifier.height(20.dp))
        Text(text = "Go to $string")

    }
}

@Composable
fun AppHeader(
    text: String,
    modifier: Modifier = Modifier,
    backgroundColor: Color = Red40,
    textColor: Color = Color.White
) {
    Box(
        modifier = modifier
            .fillMaxWidth()
            .background(backgroundColor)
            .padding(top = 30.dp)
    ) {
        Text(
            modifier = Modifier.padding(16.dp),
            text = text,
            color = textColor,
            fontSize = 22.sp
        )
    }
}
